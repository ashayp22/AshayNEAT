package mygame;

/**
 *
 * @author ashay
 */
public class Vector2 { //a vector in 2 dimension
    
    private float x;
    private float y;
    
    public Vector2(float x, float y) {
        this.x = x;
        this.y = y;
    }
    
    public float getX() {
        return x;
    }
    
    public float getY() {
        return y;
    }
    
    
    
}
